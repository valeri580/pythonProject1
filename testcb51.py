# –ò–º–ø–æ—Ä—Ç –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã—Ö –±–∏–±–ª–∏–æ—Ç–µ–∫:
# requests - –¥–ª—è HTTP-–∑–∞–ø—Ä–æ—Å–æ–≤ –∫ Bitrix24 API
# telegram - –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å Telegram Bot API
# telegram.ext - –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ –∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç—ã –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏–π
import requests
from dotenv import load_dotenv
from telegram import Update
from telegram.ext import Updater, CommandHandler, MessageHandler, Filters, CallbackContext
import os

# –ó–∞–≥—Ä—É–∑–∫–∞ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è
load_dotenv()

# –ö–æ–Ω—Å—Ç–∞–Ω—Ç—ã
TELEGRAM_TOKEN = os.getenv("VALERIADMIN_TG_BOT_API_KEY")
# BITRIX_WEBHOOK_URL - URL –≤–µ–±—Ö—É–∫–∞ Bitrix24 —Å —É–∫–∞–∑–∞–Ω–∏–µ–º –¥–æ–º–µ–Ω–∞ –∏ –∫–æ–¥–∞ –¥–æ—Å—Ç—É–ø–∞
BITRIX_WEBHOOK_URL = "https://b24-kto6at.bitrix24.ru/rest/1/sek8m0hyzx0e4z3l/"

# –§—É–Ω–∫—Ü–∏—è —Å–æ–∑–¥–∞–Ω–∏—è –ª–∏–¥–∞ –≤ Bitrix24
# –ü—Ä–∏–Ω–∏–º–∞–µ—Ç –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã–µ –ø–∞—Ä–∞–º–µ—Ç—Ä—ã name –∏ phone, –∏ –æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω—ã–π comment
def create_bitrix_lead(name: str, phone: str, comment: str = "") -> dict:
    # –§–æ—Ä–º–∏—Ä—É–µ–º –ø–æ–ª–Ω—ã–π URL –¥–ª—è API-–∑–∞–ø—Ä–æ—Å–∞ –¥–æ–±–∞–≤–ª–µ–Ω–∏—è –ª–∏–¥–∞
    url = f"{BITRIX_WEBHOOK_URL}crm.lead.add.json"

    # –ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏:
    # TITLE - –∑–∞–≥–æ–ª–æ–≤–æ–∫ –ª–∏–¥–∞ —Å –∏–º–µ–Ω–µ–º –∫–ª–∏–µ–Ω—Ç–∞
    # NAME - –∏–º—è –∫–æ–Ω—Ç–∞–∫—Ç–∞
    # PHONE - —Å–ø–∏—Å–æ–∫ —Ç–µ–ª–µ—Ñ–æ–Ω–æ–≤ (–≤ –¥–∞–Ω–Ω–æ–º —Å–ª—É—á–∞–µ –æ–¥–∏–Ω)
    # COMMENTS - –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è —Å –ø–æ–º–µ—Ç–∫–æ–π –æ –∏—Å—Ç–æ—á–Ω–∏–∫–µ –∑–∞—è–≤–∫–∏
    data = {
        "fields": {
            "TITLE": f"–ó–∞—è–≤–∫–∞ –æ—Ç {name}",
            "NAME": name,
            "PHONE": [{"VALUE": phone}],
            "COMMENTS": f"–ò—Å—Ç–æ—á–Ω–∏–∫: Telegram Bot\n{comment}"
        }
    }

    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º POST-–∑–∞–ø—Ä–æ—Å –∫ API Bitrix24 —Å –¥–∞–Ω–Ω—ã–º–∏ –≤ —Ñ–æ—Ä–º–∞—Ç–µ JSON
    response = requests.post(url, json=data)

    # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –æ—Ç–≤–µ—Ç –æ—Ç —Å–µ—Ä–≤–µ—Ä–∞ –≤ —Ñ–æ—Ä–º–∞—Ç–µ JSON
    return response.json()


# –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–æ–º–∞–Ω–¥—ã /start - –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ
def start(update: Update, context: CallbackContext):
    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ —Å –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏—è–º–∏:
    # - –ò—Å–ø–æ–ª—å–∑—É–µ–º HTML-—Ä–∞–∑–º–µ—Ç–∫—É –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è
    # - –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø—Ä–∏–º–µ—Ä –≤–≤–æ–¥–∞ –¥–∞–Ω–Ω—ã—Ö
    update.message.reply_text(
        "üìù –û—Ç–ø—Ä–∞–≤—å—Ç–µ –¥–∞–Ω–Ω—ã–µ –≤ —Ñ–æ—Ä–º–∞—Ç–µ:\n"
        "<b>–ò–º—è –¢–µ–ª–µ—Ñ–æ–Ω [–ö–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π]</b>\n\n"
        "–ü—Ä–∏–º–µ—Ä: <code>–ò–≤–∞–Ω +79123456789 –•–æ—á—É —É–∑–Ω–∞—Ç—å –ø—Ä–æ —Å–∫–∏–¥–∫–∏</code>",
        parse_mode="HTML"
    )


# –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –æ–±—ã—á–Ω—ã—Ö —Ç–µ–∫—Å—Ç–æ–≤—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π
def handle_message(update: Update, context: CallbackContext):
    try:
        # –†–∞–∑–±–∏–≤–∞–µ–º —Ç–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è –Ω–∞ —á–∞—Å—Ç–∏:
        # 1. name - –ø–µ—Ä–≤–æ–µ —Å–ª–æ–≤–æ
        # 2. phone - –≤—Ç–æ—Ä–æ–µ —Å–ª–æ–≤–æ
        # 3. comment (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ) - –≤—Å–µ –æ—Å—Ç–∞–ª—å–Ω–æ–µ
        text = update.message.text.split(maxsplit=2)
        name, phone = text[0], text[1]
        comment = text[2] if len(text) > 2 else ""

        # –°–æ–∑–¥–∞–µ–º –ª–∏–¥ –≤ Bitrix24, –ø–µ—Ä–µ–¥–∞–≤–∞—è –ø–æ–ª—É—á–µ–Ω–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
        result = create_bitrix_lead(name, phone, comment)

        # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç:
        # –ï—Å–ª–∏ –µ—Å—Ç—å –ø–æ–ª–µ 'result' - –∑–∞—è–≤–∫–∞ —Å–æ–∑–¥–∞–Ω–∞ —É—Å–ø–µ—à–Ω–æ
        if 'result' in result:
            update.message.reply_text(f"‚úÖ –ó–∞—è–≤–∫–∞ —Å–æ–∑–¥–∞–Ω–∞!\nID: {result['result']}")
        else:
            # –ï—Å–ª–∏ –æ—à–∏–±–∫–∞ - –ø–æ–ª—É—á–∞–µ–º –æ–ø–∏—Å–∞–Ω–∏–µ –∏–ª–∏ –∏—Å–ø–æ–ª—å–∑—É–µ–º —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ
            error = result.get('error_description', '–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è –æ—à–∏–±–∫–∞')
            update.message.reply_text(f"‚ùå –û—à–∏–±–∫–∞: {error}")

    except Exception as e:
        # –í —Å–ª—É—á–∞–µ –æ—à–∏–±–∫–∏ —Ñ–æ—Ä–º–∞—Ç–∞ —Å–æ–æ–±—â–µ–Ω–∏—è (–Ω–∞–ø—Ä–∏–º–µ—Ä, –Ω–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –¥–∞–Ω–Ω—ã—Ö)
        # –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é –ø—Ä–∏–º–µ—Ä –ø—Ä–∞–≤–∏–ª—å–Ω–æ–≥–æ –≤–≤–æ–¥–∞
        update.message.reply_text("‚ö†Ô∏è –û—à–∏–±–∫–∞ —Ñ–æ—Ä–º–∞—Ç–∞. –ü—Ä–∏–º–µ—Ä:\n<code>–ò–≤–∞–Ω +79123456789</code>", parse_mode="HTML")


# –û—Å–Ω–æ–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ –∏ –∑–∞–ø—É—Å–∫–∞ –±–æ—Ç–∞
def main():
    # –°–æ–∑–¥–∞–µ–º —ç–∫–∑–µ–º–ø–ª—è—Ä Updater –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å Telegram API
    updater = Updater(TELEGRAM_TOKEN)

    # –ü–æ–ª—É—á–∞–µ–º –¥–∏—Å–ø–µ—Ç—á–µ—Ä –¥–ª—è —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏–∏ –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤
    dp = updater.dispatcher

    # –†–µ–≥–∏—Å—Ç—Ä–∏—Ä—É–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏:
    # 1. –î–ª—è –∫–æ–º–∞–Ω–¥—ã /start
    # 2. –î–ª—è –æ–±—ã—á–Ω—ã—Ö —Ç–µ–∫—Å—Ç–æ–≤—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π (–∏—Å–∫–ª—é—á–∞—è –∫–æ–º–∞–Ω–¥—ã)
    dp.add_handler(CommandHandler("start", start))
    dp.add_handler(MessageHandler(Filters.text & ~Filters.command, handle_message))

    # –í—ã–≤–æ–¥–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ –∑–∞–ø—É—Å–∫–µ –≤ –∫–æ–Ω—Å–æ–ª—å
    print("ü§ñ –ë–æ—Ç –∑–∞–ø—É—â–µ–Ω...")

    # –ó–∞–ø—É—Å–∫–∞–µ–º –±–æ—Ç–∞ –≤ —Ä–µ–∂–∏–º–µ polling (–ø–æ—Å—Ç–æ—è–Ω–Ω—ã–π –æ–ø—Ä–æ—Å —Å–µ—Ä–≤–µ—Ä–∞ Telegram)
    updater.start_polling()

    # –û—Å—Ç–∞–≤–ª—è–µ–º –±–æ—Ç–∞ —Ä–∞–±–æ—Ç–∞—Ç—å –¥–æ –ø—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–æ–π –æ—Å—Ç–∞–Ω–æ–≤–∫–∏
    updater.idle()


# –°—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–∞—è –ø—Ä–æ–≤–µ—Ä–∫–∞ –¥–ª—è –∑–∞–ø—É—Å–∫–∞ –∫–æ–¥–∞ —Ç–æ–ª—å–∫–æ –ø—Ä–∏ –ø—Ä—è–º–æ–º –≤—ã–∑–æ–≤–µ —Ñ–∞–π–ª–∞
if __name__ == '__main__':
    main()
